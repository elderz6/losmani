	  <img src="imgs/mousse.jpg" class="fotodesu" />
	  </h3>
      <p>
	  
	  
	  <button data-toggle="collapse" data-target="#ing2" class="ingredientes">Ingredientes</button>

<div id="ing2" class="collapse">
•	1 lata de leite condensado <br />
•	2 latas de leite <br />
•	2 colheres cheias de amido de milho <br />
•	6 ovos (claras e gemas separadas) <br />
•	200 gr de amendoim torrado e moído <br />
•	1 lata de creme de leite sem soro <br />
•	12 colheres de açúcar <br />
•	1 colher de sopa de essência de baunilha <br />


</div>

 <button data-toggle="collapse" data-target="#prep2" class="ingredientes">Modo de Preparo</button>

<div id="prep2" class="collapse">
1.	Separe uma panela para fazer um creme com o leite condensado, o leite, as gemas e o amido de milho. <br />
2.	No fogo baixo, adicione à panela esses ingredientes que mencionamos e mexa até virar um creme consistente e homogêneo. Deixe esfriar; <br />
3.	Depois que o creme esfriar, junte o amendoim torrado e moído. Mexa bem e reserve; <br />
4.	Bata as claras até ficarem em neve, adicione o açúcar e a essência de baunilha. Depois, bata mais um pouco com a ajuda da batedeira; <br />
5.	Adicione à mistura o creme de leite sem soro e mexa devagar; <br />
6.	Em seguida adicione o creme com o amendoim e mexa delicadamente; <br />
7.	Agora coloque o mousse em um recipiente e leve para gelar por 3 horas; <br />
8.	Depois é só servir.


</div>

