	  <button data-toggle="collapse" data-target="#ing1" class="ingredientes">Ingredientes</button>

<div id="ing1" class="collapse">
•	4 ovos (claras e gemas separadas)   <br />
•	1 1/2 xícara (chá) de açúcar    <br />
•	1 xícara (chá) de leite   <br />
•	2 xícaras (chá) de farinha de trigo   <br />
•	1 colher (chá) de fermento em pó   <br />
•	1/4 de xícara (chá) de amendoim torrado e bem moído  <br />
Recheio:  <br />
 
•	2 latas de leite condensado   <br />
•	1/2 xícara (chá) de amendoim torrado e moído   <br />
•	1/2 xícara (chá) de creme de leite  <br />

</div>
	  
 <button data-toggle="collapse" data-target="#prep1" class="ingredientes">Modo de Preparo</button>

<div id="prep1" class="collapse">
Modo de preparo  <br />
1.	Bata as claras em neve e reserve.  <br />
2.	Bata o açúcar, as gemas e acrescente o leite.   <br />
3.	Aos poucos, coloque a farinha e o fermento peneirados.  <br />
4.	Adicione as claras em neve com o amendoim e misture bem.  <br />
5.	Transfira para uma forma untada e asse até dourar.  <br />
Recheio:  <br />

1.	Em uma panela, ponha o leite condensado e o amendoim.  <br />
2.	Leve ao fogo baixo, cozinhando até engrossar. Retire do fogo.  <br />
3.	Junte o creme de leite e misture bem.  <br />
4.	Deixe esfriar, corte o bolo ao meio, recheie com metade do creme.  <br />
5.	Cubra com o creme restante.  <br />
6.	Se desejar, decore com mais amendoim picado.  <br />

</div>

