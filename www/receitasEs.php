      <p>
	  
	  
	  <button data-toggle="collapse" data-target="#ing2" class="ingredientes">Ingredientes</button>

<div id="ing2" class="collapse">
•	1 lata de leche condensada <br />
• 2 latas de leche <br />
• 2 cucharadas de fécula de maíz llena <br />
• 6 huevos (claras y las yemas separadas) <br />
• 200 g de maní tostado y molido <br />
• 1 Crema agria suero libre puede <br />
• 12 cucharadas de azúcar <br />
• 1 cucharadita de esencia de vainilla sopa <br />
</div>

 <button data-toggle="collapse" data-target="#prep2" class="ingredientes">Modo de Preparo</button>

<div id="prep2" class="collapse">

1. dejar de lado una olla para hacer una crema con la leche condensada, la leche, las yemas de huevo y almidón de maíz.  <br />
2. En fuego lento, añadir a la sartén estos ingredientes mencionados y remover a su vez una crema consistente y homogénea. Deje que se enfríe;  <br />
3. Una vez que la crema se haya enfriado, añadir los cacahuetes tostados y molidos. Remover bien y dejar de lado;  <br />
4. Batir las claras de huevo a punto de nieve, añadir el azúcar y la esencia de vainilla. A continuación, batir un poco más con la ayuda de una batidora eléctrica;  <br />
5. Añadir a la mezcla de la crema agria libre de suero y se agita lentamente;  <br />
6. A continuación, agregue la crema con los cacahuetes y se agita suavemente;  <br />
7. Ahora ponga la mousse en un recipiente y llevar a congelar durante 3 horas;  <br />
8. A continuación, sólo sirven.


