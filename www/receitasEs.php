<div id="ing1" class="collapse">
• 4 huevos (claras y las yemas separadas) <br />
• 1 1/2 taza de azúcar (té) <br />
• 1 taza (té) de leche <br />
• 2 tazas (té) de harina de trigo <br />
• 1 cucharadita (tsp) de polvo de hornear <br />
• 1/4 taza (té) de maní tostado y molido así <br />
relleno: <br />
• 2 latas de leche condensada <br />
• 1/2 taza (té) de maní tostado y molido <br />
• 1/2 taza (té) de crema <br />

</div>
 <button data-toggle="collapse" data-target="#prep1" class="ingredientes">Modo de Preparo</button>

<div id="prep1" class="collapse">
Modo de preparo <br />
1. Batir las claras de huevo y dejar de lado. <br />
2. Batir el azúcar, las yemas de huevo y añadir la leche. <br />
3. Poco a poco, poner la harina y la levadura en polvo cernida. <br />
4. Añadir las claras de huevo con los cacahuetes y mezclar bien. <br />
5. Pasar a un molde engrasado y hornear hasta que estén doradas. <br />
relleno: <br />
1. En una cacerola, colocar la leche condensada y los cacahuetes. <br />
2. Llevar a fuego lento, cocinar hasta que espese. Retirar del fuego. <br />
3. Añadir la nata y mezclar bien. <br />
4. Dejar enfriar, cortar el pastel por la mitad, rellenar con la mitad de la crema. <br />
5. Cubrir con la crema restante. <br />
6. Si se desea, decorar con los cacahuetes más picadas. <br />
</div>

