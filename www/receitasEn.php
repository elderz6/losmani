      <h3>
	  <img src="imgs/bolo.jpg" class="fotodesu" />
	  </h3>
      <p>
	  <button data-toggle="collapse" data-target="#ing1" class="ingredientes">Ingredients</button>

<div id="ing1" class="collapse">
• 4 eggs (whites and yolks separated) <br />
• 1 1/2 cup (tea) sugar <br />
• 1 cup (tea) of milk <br />
• 2 cups (tea) of wheat flour <br />
• 1 teaspoon (tsp) baking powder <br />
• 1/4 cup (tea) of roasted peanuts and well ground <br />
Filling: <br />
•	2 cans of condensed milk <br />
• 1/2 cup (tea) of roasted peanuts and ground <br /> 
• 1/2 cup (tea) cream <br /> 

</div>
	  
 <button data-toggle="collapse" data-target="#prep1" class="ingredientes">Method of preparation</button>

<div id="prep1" class="collapse">
1. Beat the egg whites and set aside. <br /> 
2. Beat the sugar, egg yolks and add the milk. <br /> 
3. Gradually, put the flour and baking powder sifted. <br /> 
4. Add the egg whites with the peanuts and mix well. <br /> 
5. Transfer to a greased pan and bake until golden brown. <br /> 
Filling: <br /> 
1. In a saucepan, place the condensed milk and peanuts. <br /> 
2. Bring to low heat, cooking until thickened. Remove from heat. <br /> 
3. Add the cream and mix well. <br /> 
4. Let cool, cut the cake in half, fill with half the cream. <br /> 
5. Cover with the remaining cream. <br /> 
6. If desired, garnish with more chopped peanuts. <br /> 

</div>

