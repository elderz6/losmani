      <p>
	  
	  
	  <button data-toggle="collapse" data-target="#ing2" class="ingredientes">Ingredients</button>

<div id="ing2" class="collapse">
•	1 can of condensed milk <br />
• 2 milk cans <br />
• 2 tablespoons cornstarch filled <br />
• 6 eggs (whites and yolks separated) <br />
• 200 g roasted peanuts and ground <br />
• 1 serum-free sour cream can <br />
• 12 tablespoons sugar <br />
• 1 tsp vanilla essence  <br />


</div>

 <button data-toggle="collapse" data-target="#prep2" class="ingredientes">Method of preparation</button>

<div id="prep2" class="collapse">
1. Set aside a pot to make a cream with condensed milk, milk, egg yolks and cornstarch.  <br />
2. In low heat, add to pan these ingredients mentioned and stir to turn a consistent and homogeneous cream. Let cool;  <br />
3. Once the cream has cooled, add the roasted and ground peanuts. Stir well and set aside;  <br />
4. Beat the egg whites until snow, add sugar and vanilla essence. Then beat a little more with the help of an electric mixer;  <br />
5. Add to the mix the serum-free sour cream and stir slowly;  <br />
6. Then add the cream with the peanuts and stir gently;  <br />
7. Now put the mousse in a container and bring to freeze for 3 hours;  <br />
8. Then just serve.

